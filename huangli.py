JIE_MONTHS = {"立春","惊蛰","清明","立夏","芒种","小暑","立秋","白露","寒露","立冬","大雪","小寒"}

# ── 接口函数 ──────────────────────────────────────────

def get_yi_ji(day_gan: str, day_zhi: str) -> dict:
    """返回 {yi: [...], ji: [...]}"""
    return YI_JI.get((day_gan, day_zhi), {"yi": ["诸事不宜"], "ji": []})

def get_jieqi_detail(jieqi_name: str) -> dict | None:
    """返回节气详情，如果不是节气日则返回 None"""
    return JIEQI_DETAIL.get(jieqi_name)

def is_jieqi_day(d: date) -> bool:
    """当天是否为二十四节气日"""
    return get_jieqi(d) is not None

def is_jie_month_change(d: date) -> bool:
    """当天是否为'节'（换月点，非'气'）"""
    name = get_jieqi(d)
    return name is not None and name in JIE_MONTHS


