# ── 计算函数 ──────────────────────────────────────────

def _is_leap(y: int) -> bool:
    """Gregorian 闰年判定"""
    return y % 4 == 0 and (y % 100 != 0 or y % 400 == 0)

def _days_in_year(y: int) -> int:
    return 366 if _is_leap(y) else 365

def days_from_1900(y: int, m: int, d: int) -> int:
    """计算给定日期距离 1900-01-01 的天数（仅用于日柱计算）。"""
    days = 0
    for year in range(1900, y):
        days += _days_in_year(year)
    month_days = [0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]
    if _is_leap(y):
        month_days[2] = 29
    for mo in range(1, m):
        days += month_days[mo]
    days += (d - 1)  # 1900-01-01 是第 0 天
    return days

def day_ganzhi(d: date) -> tuple[str, str]:
    """日柱: 基准 1900-01-01 = 甲戌 (干0, 支10)"""
    diff = days_from_1900(d.year, d.month, d.day)
    gan_idx = (0 + diff) % 10
    zhi_idx = (10 + diff) % 12
    return TIAN_GAN[gan_idx], DI_ZHI[zhi_idx]

def year_ganzhi(d: date) -> tuple[str, str]:
    """年柱: (year - 4) % 10 → 年干, (year - 4) % 12 → 年支。立春前属上年。"""
    y = d.year
    # 如果日期在立春之前，使用上一年
    if d.month < 2 or (d.month == 2 and d.day < 4):
        y -= 1
    gan = TIAN_GAN[(y - 4) % 10]
    zhi = DI_ZHI[(y - 4) % 12]
    return gan, zhi

def month_ganzhi(d: date) -> tuple[str, str]:
    """月柱: 节气月分界 + 五虎遁从年干推月干。

    节气月: 立春→寅月, 惊蛰→卯月, ..., 小寒→丑月。
    十二年节分界 list — 每月对应当年月日边界。
    """
    y = d.year
    # 十二"节"边界（月,日,月支），按时间顺序排列。
    # 注意: 小寒落在次年1月，因此丑月边界可能跨年。
    boundaries = [
        (date(y, 2, 4), "寅"),   # 立春
        (date(y, 3, 5), "卯"),   # 惊蛰
        (date(y, 4, 4), "辰"),   # 清明
        (date(y, 5, 5), "巳"),   # 立夏
        (date(y, 6, 5), "午"),   # 芒种
        (date(y, 7, 7), "未"),   # 小暑
        (date(y, 8, 7), "申"),   # 立秋
        (date(y, 9, 7), "酉"),   # 白露
        (date(y, 10, 8), "戌"),  # 寒露
        (date(y, 11, 7), "亥"),  # 立冬
        (date(y, 12, 7), "子"),  # 大雪
        (date(y + 1, 1, 5), "丑"),  # 小寒(次年)
    ]

    # 从后往前找 — 取最近一个 <= d 的边界
    month_zhi = "丑"  # fallback: Jan 1-4 属于丑月（上年小寒后）
    for boundary_date, zhi in boundaries:
        if d >= boundary_date:
            month_zhi = zhi

    # 五虎遁推月干
    year_gan, _ = year_ganzhi(d)
    start_gan_for_month1 = WU_HU_DUN[year_gan]
    zhi_order = {"寅": 0, "卯": 1, "辰": 2, "巳": 3, "午": 4, "未": 5,
                 "申": 6, "酉": 7, "戌": 8, "亥": 9, "子": 10, "丑": 11}
    offset = zhi_order[month_zhi]
    gan_idx = (TIAN_GAN.index(start_gan_for_month1) + offset) % 10
    return TIAN_GAN[gan_idx], month_zhi

# 小时 → 时辰地支映射 (23:00-01:00=子时, 01:00-03:00=丑时, ...)
HOUR_TO_ZHI = [
    "子","丑","丑","寅","寅","卯","卯",  # 0-6
    "辰","辰","巳","巳","午","午",        # 7-12
    "未","未","申","申","酉","酉",        # 13-18
    "戌","戌","亥","亥","子",             # 19-23
]

def hour_ganzhi(hour: int, day_stem: str) -> tuple[str, str]:
    """时柱: 小时→时辰地支 + 五鼠遁从日干推时干。

    时辰: 子(23-1), 丑(1-3), 寅(3-5), ..., 亥(21-23)
    """
    zhi = HOUR_TO_ZHI[hour]
    zhi_idx = DI_ZHI.index(zhi)

    start_gan = WU_SHU_DUN[day_stem]
    gan_idx = (TIAN_GAN.index(start_gan) + zhi_idx) % 10
    return TIAN_GAN[gan_idx], zhi

def get_nayin(gan: str, zhi: str) -> str:
    """查纳音表"""
    return NAYIN_TABLE.get((gan, zhi), "未知")

def get_jieqi(d: date) -> str | None:
    """如果当天是节气日，返回节气名称。"""
    key = (d.month, d.day)
    return ALL_JIEQI.get(key)

def get_zodiac(zhi: str) -> str:
    """地支 → 生肖"""
    return ZODIAC[DI_ZHI.index(zhi)]
